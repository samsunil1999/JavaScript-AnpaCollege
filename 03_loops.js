// for loop
for (let i = 1; i <= 5; i++) {
    console.log("for loop, i = ",i);
}

console.log("for loop has ended....")

// while loop
let j = 1;
while (j<=5) {
    console.log("while loop, j = ",j);
    j++;
}

console.log("while has ended...")

//  dod while loop
let k = 20;
do {
    console.log("do while loop, k = ",k);
    k++;
} while(k<=5);

console.log("do while loop has ended....")